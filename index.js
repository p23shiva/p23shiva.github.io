function getBarColor(value, maxValue = 5) {
    // Min and max values
    var minVal = 1;
    var maxVal = maxValue;

    // Light blue (min color) and dark blue (max color)
    var minColor = [128, 128, 255]; // RGB for light blue (#e6f7ff)
    var maxColor = [255, 255, 255];   // RGB for dark blue (#0066cc)
    var factor = 1;
    minColor = minColor.map(i => Math.round(i / factor));
    maxColor = maxColor.map(i => Math.round(i / factor));
    // Normalize value between 0 and 1
    var ratio = (value - minVal) / (maxVal - minVal);

    // Interpolate between minColor and maxColor
    var r = Math.round(minColor[0] + ratio * (maxColor[0] - minColor[0]));
    var g = Math.round(minColor[1] + ratio * (maxColor[1] - minColor[1]));
    var b = Math.round(minColor[2] + ratio * (maxColor[2] - minColor[2]));

    // Return the color in hex format
    return rgbToHex(r, g, b);
}
function rgbToHex(r, g, b) {
    return '#' + [r, g, b].map(function (x) {
        var hex = x.toString(16);
        return hex.length === 1 ? '0' + hex : hex;
    }).join('');
}
