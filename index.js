function init() {
    document.getElementById('filterValue').oninput = function () { document.getElementById('filterValueShow').innerHTML = this.value; filterData(); }
    let str = ''; for (let i = 0; i < areas?.length; i++) str += ('<option value="' + areas[i] + '">' + areas[i] + '</option>');
    document.getElementById('areaI').innerHTML += str;
    let str1 = ''; for (let i = 0; i < profs?.length; i++) str1 += ('<option value="' + profs[i] + '">' + profs[i] + '</option>');
    document.getElementById('prof').innerHTML += str1;
}
function filterData() {
    let area = document.getElementById('areaI')?.value, prof = document.getElementById('prof')?.value, rat = document.getElementById('filter')?.value, filterVal = document.getElementById('filterValue')?.value;
    let currArr = totalData;
    if (area && area != 'all') currArr = currArr?.filter(e => e?.area == area);
    if (prof && prof != 'all') currArr = currArr?.filter(e => (e?.instructor?.toLocaleLowerCase()?.includes(prof?.toLocaleLowerCase()) || e?.course?.toLocaleLowerCase()?.includes(prof?.toLocaleLowerCase())));
    if (rat && rat != 'all') currArr = currArr?.filter(e => e?.[rat] > filterVal);
    if (rat && rat != 'all') currArr?.sort((i, j) => j?.[rat] - i?.[rat]);
    populateTable(currArr);
}